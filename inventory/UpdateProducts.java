    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        int checkProductExist=0;// -> code ?? logic??
        String uniqueId = txtProductId.getText();
        if(uniqueId.equals("")){
            JOptionPane.showMessageDialog(null, "Product Id is required!!!");
        }
        else{
                SimpleDateFormat dFormat= new SimpleDateFormat("dd-MM-yyyy"); 
            try{
                Connection con = Connect.getCon();
                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery("select *from product where uniqueId="+uniqueId+"");
                
                while(rs.next()){
                    txtProductId.setEditable(false);
                    txtName.setText(rs.getString("name"));
                    txtQuantity.setText(rs.getString("quantity"));
                    txtPricePerUnit.setText(rs.getString("price"));
                    String Manufacturing_date = rs.getString("Manufacturing_date");
                    String expiry_date = rs.getString("expiry_date");
                    if(Manufacturing_date !=""){
                        try{
                            dateManufacturingDate.setDate(dFormat.parse(Manufacturing_date));
                        }
                        catch(Exception e){
                        }
                    }
                    if(expiry_date !=""){
                        try{
                            dateExpiryDate.setDate(dFormat.parse(expiry_date));
                        }
                        catch(Exception e){
                        }
                    }
                    checkProductExist=1;
                }
                
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            if(checkProductExist == 0){
                JOptionPane.showMessageDialog(null, "Product Id does not exist");
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

