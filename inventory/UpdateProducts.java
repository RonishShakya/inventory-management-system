    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dFormat= new SimpleDateFormat("dd-MM-yyyy"); 
        String uniqueId = txtProductId.getText();
        String name = txtName.getText();
        String quantity = txtQuantity.getText();
        String price = txtPricePerUnit.getText();
        String addQuantity = txtAddQuantity.getText();
        
        int totalQuantity;
        totalQuantity = 0;
        if(addQuantity.equals("")){
            totalQuantity = Integer.parseInt(quantity);
        }else{
            totalQuantity = Integer.parseInt(quantity) + Integer.parseInt(addQuantity);
        }
        
                Date Manufacturing_date= dateManufacturingDate.getDate();
        Date expiry_date= dateExpiryDate.getDate();
        String manufacturing_date="";
         String Expiry_date="";
         if(Manufacturing_date != null){
               manufacturing_date=dFormat.format(dateManufacturingDate.getDate());
         }
          if(expiry_date!=null){
               Expiry_date=dFormat.format(dateExpiryDate.getDate());
              }
          if(uniqueId.equals("")){
           JOptionPane.showMessageDialog(null, "Product ID is required");
           }else if(name.equals("")){
               JOptionPane.showMessageDialog(null, "Name is required");
           }else if(quantity.equals("")){
               JOptionPane.showMessageDialog(null, "Quantity is required");
            }else if(!addQuantity.matches(numberPattern)){
              JOptionPane.showMessageDialog(null, "Quantity is Invalid");
           }else if(price.equals("")){
               JOptionPane.showMessageDialog(null, "Price Per Unit is required");
           }else if(!price.matches(numberPattern)){
              JOptionPane.showMessageDialog(null, "Price Per Unit is Invalid"); 
           }else if(Manufacturing_date.equals("")){
               JOptionPane.showMessageDialog(null, "Manufacturing Date is required");
           }else if(expiry_date.equals("")){
                JOptionPane.showMessageDialog(null, "Expiry Date is required");
           }else{ 
                    try{
                        Connection con = Connect.getCon();
                        PreparedStatement ps = con.prepareStatement(" update product set name=?,quantity = ?,price =?, Manufacturing_date = ?,expiry_date = ? where uniqueId = ?");
                  
                   ps.setString(1, name);
                   ps.setInt(2, totalQuantity );
                   ps.setString(3, price);
                   ps.setString(4, manufacturing_date);
                   ps.setString(5, Expiry_date);
                    ps.setInt(6, Integer.parseInt(uniqueId));
                   ps.executeUpdate();
                   JOptionPane.showMessageDialog(null, "Product Updated Successfully");
                   setVisible(false);
                    new UpdateProducts().setVisible(true);            
                    }
                    catch(Exception e){
                        JOptionPane.showMessageDialog(null, e);
                    }
    }//GEN-LAST:event_jButton8ActionPerformed
    }
