                    model.addRow(new Object[]{rs.getString("product_pk"), rs.getString("uniqueId"), rs.getString("name"), rs.getString("quantity"), rs.getString("price"), rs.getString("Manufacturing_date"), rs.getString("expiry_date")});//yei ho?
