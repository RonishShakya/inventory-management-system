    private void btnCheckStockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckStockActionPerformed
        // TODO add your handling code here:
        final int alertLimit = 50;
        txtMessage.setEditable(false);
//        StringBuilder alertMessage = new StringBuilder();
        String msg = "";
        try {
            Connection con = Connect.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(" SELECT * FROM product where quantity < 50 "); // yesma hola qurrey hera where ko
            boolean alertFound = false;

            while (rs.next()) {
                int productId = rs.getInt("uniqueID");// this one
                String itemName = rs.getString("name");// this one
                int quantity = rs.getInt("quantity");// this

                if (quantity < alertLimit) {
                    msg = " Alert: Low quantity level of product " + itemName +   "\nCurrent Quantity:" + quantity + "\n"; // .thapne + garera ?? esmai add garne ho?? maybe..
                    // add garana kun kun chahincha vane ko bujena aayena malai jun jun ko quantity level kam xa tyo product haru dekhunu parne ho
                    // ani teta \n chaldaina aba single line mai huncha
                    
                    alertFound = true;
//                alertMessage.append(Message);
                }
            }
            rs.close();

            con.close();
            if (alertFound) {
                txtMessage.setText(msg); // Display the accumulated alert messages
            } else {
                txtMessage.setText("No inventory alerts."); // Display a message if no alerts are found
            }
        } catch (Exception e) {
//           JOptionPane.showMessageDialog(null, e);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnCheckStockActionPerformed

